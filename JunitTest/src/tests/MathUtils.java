package tests;

public class MathUtils {
    public int subtract(int x, int y) {
    	return x -y;
    }
    
    public int add (int x , int y) {
    	return x + y;
    }
    
    public int multiply(int x , int y) {
    	return x * y;
    }
    
    public int square(int x) {
    	return x*x;
    }
}